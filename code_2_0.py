print("Welcome to the tip calculator!")
bill_amount = float(input("what's the total bill amount? Rs. "))
tip = int(input("what's tip amount you like to give? 10, 12, 15? "))
person = int(input("how many people's are sharing the bill? "))

tip_percentage = tip / 100
total_amount = bill_amount * tip_percentage
total_bill = bill_amount + total_amount
each_share = total_bill / person
amount = round(each_share)

print(f" each person share is: Rs{amount} ")
